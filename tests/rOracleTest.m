            runOne=@(test) gofcopula.runTest(test,d3Case.Name, ...
                oracle.statisticSample3,M=0,Param=entry.theta, ...
                ParamEst=false,DFEst=false,Margins="none", ...
                NumericMode="rCompatible").Tests.Statistic;
            testCase.verifyEqual(runOne("gofCvM"),entry.cvm,AbsTol=1e-10);
            testCase.verifyEqual(runOne("gofKS"),entry.ks,AbsTol=1e-9);
            testCase.verifyEqual(runOne("gofRosenblattSnB"),entry.rosenblattSnB,AbsTol=1e-9);
            testCase.verifyEqual(runOne("gofRosenblattSnC"),entry.rosenblattSnC,AbsTol=1e-9);
            testCase.verifyEqual(runOne("gofRosenblattGamma"),entry.rosenblattGamma,AbsTol=1e-8);
            testCase.verifyEqual(runOne("gofRosenblattChisq"),entry.rosenblattChisq,AbsTol=1e-8);
        end

