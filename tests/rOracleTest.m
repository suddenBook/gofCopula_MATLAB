        function kendallStatisticMatchesR(testCase,kendallCase)
            % The Kendall statistics against the frozen 500-draw reference
            % copula values, exactly as gofCopula's bootstrap consumes them.
            oracle=testCase.Oracle;
            options=struct(ReferenceCopulaValues=oracle.kendallReference, ...
                NumericMode="rCompatible");
            model=gofcopula.CopulaModel("clayton",Theta=1.4,EstimateTheta=false);
            actual=gofcopula.internal.statistics.computeStatistic( ...
                kendallCase.Method,oracle.statisticSample,model,options);
            testCase.verifyEqual(actual, ...
                oracle.statistics.(kendallCase.Field),AbsTol=1e-12);
        end

        function trivariateFamilyMatchesR(testCase,d3Case)
            oracle=testCase.Oracle;
            entry=oracle.d3.(d3Case.Name);
            dispersion="unstructured";
            if d3Case.Name=="normal", dispersion="exchangeable"; end
            cdf=gofcopula.copulaCDF(d3Case.Name,oracle.evaluationPoints3, ...
                entry.theta,Dispersion=dispersion);
            pdf=gofcopula.copulaPDF(d3Case.Name,oracle.evaluationPoints3, ...
                entry.theta,Dispersion=dispersion);
            Z=gofcopula.rosenblatt(d3Case.Name,oracle.evaluationPoints3, ...
                entry.theta,Dispersion=dispersion);
            % R's pmvnorm in d=3 is randomized quasi-Monte-Carlo (~1e-6);
            % the Archimedean CDFs are analytic and far tighter.
            testCase.verifyEqual(cdf,entry.cdf,AbsTol=5e-6);
            testCase.verifyEqual(pdf,entry.pdf,RelTol=5e-8);
            testCase.verifyEqual(Z,entry.rosenblatt,AbsTol=1e-9);
        end

