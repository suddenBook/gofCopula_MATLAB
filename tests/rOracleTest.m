    methods (TestClassSetup)
        function loadOracle(testCase)
            root=fileparts(fileparts(mfilename("fullpath")));
            testCase.applyFixture(matlab.unittest.fixtures.PathFixture( ...
                fullfile(root,"src")));
            fixture=fullfile(root,"tests","reference","rOracle.json");
            testCase.Oracle=jsondecode(fileread(fixture));
        end
    end

    methods (Test)
        function cdfMatchesR(testCase,familyCase)
            oracle=testCase.Oracle;
            expected=oracle.families.(familyCase.Name);
            actual=gofcopula.copulaCDF(familyCase.Name,oracle.evaluationPoints, ...
                expected.theta,DF=expected.df);
            testCase.verifyEqual(actual,expected.cdf,AbsTol=3e-12);
        end

        function pdfMatchesR(testCase,familyCase)
            oracle=testCase.Oracle;
            expected=oracle.families.(familyCase.Name);
            actual=gofcopula.copulaPDF(familyCase.Name,oracle.evaluationPoints, ...
                expected.theta,DF=expected.df);
            testCase.verifyEqual(actual,expected.pdf,AbsTol=5e-8);
        end

        function rosenblattMatchesR(testCase,rosenblattCase)
            oracle=testCase.Oracle;
            family=oracle.families.(rosenblattCase.Name);
            actual=gofcopula.rosenblatt(rosenblattCase.Name, ...
                oracle.evaluationPoints,family.theta,DF=family.df);
            expected=oracle.rosenblatt.(rosenblattCase.Name);
            testCase.verifyEqual(actual,expected,AbsTol=1e-10);
        end

