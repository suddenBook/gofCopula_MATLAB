        end

        function marginFitMatchesR(testCase,marginCase)
            % R fits margins by constrOptim maximum likelihood; the port
            % uses fitdist. Optima agree, optimizers differ, so the
            % transformed values are compared with a small-sample
            % tolerance. t and chisq margins are documented deviations
            % (different parameterizations) and are not compared.
            oracle=testCase.Oracle;
            entry=oracle.margins.(marginCase.Name);
            [u,parameters]=gofcopula.internal.estimation.transformMargins( ...
                entry.data(:),marginCase.Name);
            testCase.verifyEqual(u,entry.u,AbsTol=2e-2);
            if marginCase.CompareParameters
                actual=marginParameterVector(marginCase.Name,parameters{1});
                testCase.verifyEqual(actual,entry.parameters(:).',RelTol=5e-2);
            end
        end

        function flipConventionMatchesR(testCase,flipCase)
            % Pins the R rotation convention (90 -> (1-u2, u1), etc.).
            oracle=testCase.Oracle;
            entry=oracle.flip(flipCase.Index);
            result=gofcopula.runTest("gofCvM","frank", ...
                oracle.statisticSample,M=0,ParamEst=true,DFEst=false, ...
                Margins="none",Flip=entry.angle,NumericMode="rCompatible");
            testCase.verifyEqual(result.Theta,entry.theta,AbsTol=5e-5);
            % Optimizer-limited through the fitted parameter.
            testCase.verifyEqual(result.Tests.Statistic,entry.cvm,AbsTol=5e-8);
        end

        function tevJointEstimateMatchesR(testCase)
            % The t-EV (theta, df) likelihood has a long, nearly flat
            % ridge: R and the port stop at different ridge points with
            % indistinguishable fits. The meaningful invariants are that
            % the port's pseudo-likelihood is at least as high as at R's
            % estimate and that both estimates imply the same Pickands
            % dependence function.
            oracle=testCase.Oracle;
            result=gofcopula.runTest("gofCvM","tev",oracle.tev.sample, ...
                M=0,Param=0.3,ParamEst=true,DF=8,DFEst=true,Margins="none");
            logLik=@(theta,df) sum(gofcopula.copulaPDF("tev", ...
                min(max(oracle.tev.sample,eps),1-eps),theta,DF=df,Log=true));
            portLik=logLik(result.Theta,result.DegreesOfFreedom);
            rLik=logLik(oracle.tev.theta,oracle.tev.df);
            testCase.verifyGreaterThanOrEqual(portLik,rLik-1e-3);
            w=(0.02:0.02:0.98).';
            Aport=gofcopula.internal.copulas.evDependence("tev",w, ...
                result.Theta,result.DegreesOfFreedom);
            Ar=gofcopula.internal.copulas.evDependence("tev",w, ...
                oracle.tev.theta,oracle.tev.df);
            testCase.verifyEqual(Aport,Ar,AbsTol=2e-3);
        end
    end
end

function vector = marginParameterVector(family, parameters)
switch family
    case "exp"
        vector = 1 ./ parameters.Distribution.mu; % R stores the rate
    case "weibull"
        % R stores (shape, scale); fitdist Weibull stores (A=scale, B=shape).
        vector = [parameters.Distribution.B, parameters.Distribution.A];
    case "norm"
        vector = [parameters.Distribution.mu, parameters.Distribution.sigma];
    case "lnorm"
        vector = [parameters.Distribution.mu, parameters.Distribution.sigma];
    case "beta"
        vector = [parameters.Distribution.a, parameters.Distribution.b];
    otherwise
        error("gofcopula:test:UnexpectedMarginFamily", ...
            "No parameter mapping for %s.", family);
end
end
