            result=functionHandle("clayton",oracle.statisticSample,M=0, ...
                Param=1.4,ParamEst=false,DFEst=false,Margins="none");
            expected=oracle.statistics.(statisticCase.Field);
            testCase.verifyEqual(result.Tests.Statistic,expected,AbsTol=1e-10);
        end

