        function ranksPipelineMatchesR(testCase)
            % The rCompatible mode must reproduce R's rank-margins
            % bootstrap chain exactly: pseudo-observations for the
            % observed statistic, raw (never re-ranked) replicates.
            oracle=testCase.Oracle;
            samples=oracle.ranksPipeline.samples;
            result=gofcopula.runTest("gofCvM","clayton", ...
                oracle.ranksPipeline.rawData,M=size(samples,3), ...
                ParamEst=true,DFEst=false,Margins="ranks",Seed=1, ...
                BootstrapSamples=samples,NumericMode="rCompatible");
            testCase.verifyEqual(result.Theta, ...
                oracle.ranksPipeline.theta,AbsTol=5e-5);
            % Optimizer-limited: the fitted theta agrees to ~1e-8, which
            % propagates linearly into the statistic.
            testCase.verifyEqual(result.Tests.Statistic, ...
                oracle.ranksPipeline.observed,AbsTol=1e-8);
            testCase.verifyEqual(result.Tests.PValue, ...
                oracle.ranksPipeline.pValue,AbsTol=eps);
            % corrected mode re-ranks the same replicates: the p-value
            % must differ (this is the calibration fix).
            corrected=gofcopula.runTest("gofCvM","clayton", ...
                oracle.ranksPipeline.rawData,M=size(samples,3), ...
                ParamEst=true,DFEst=false,Margins="ranks",Seed=1, ...
                BootstrapSamples=samples,NumericMode="corrected");
            testCase.verifyNotEqual(corrected.Tests.PValue, ...
                result.Tests.PValue);
        end

        function whiteStudentModerateSampleMatchesR(testCase)
            oracle=testCase.Oracle;
            result=gofcopula.gofWhite("t",oracle.whiteT100.sample,M=0, ...
                Param=oracle.whiteT100.par,ParamEst=false, ...
                DF=oracle.whiteT100.df,DFEst=false,Margins="none", ...
                NumericMode="rCompatible");
